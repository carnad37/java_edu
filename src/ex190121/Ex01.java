package ex190121;

public class Ex01 {

	public static void main(String[] args) {
		
		int i = 0, count = 0, div = 0;
		
		
		for(count = 1; count <10; count++)
		{
			for(i = 2; i < 10; i++)
			{
				div = i*count;
				System.out.print(i+" * "+count+" = "+div+"\t");
			}
			System.out.println("");
			
		}
		
	}
	
}
