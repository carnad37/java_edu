package work.algorithm;

public class Calculator {

	public int plus(int x, int y)
	{
		int ret= x+y;
		return ret;
	}
	public int minus(int x, int y)
	{
		int ret= x-y;
		return ret;
	}
	public int mul(int x, int y)
	{
		int ret= x*y;
		return ret;
	}
	public int divide(int x, int y)
	{
		int ret= x/y;
		return ret;
	}
	
}
